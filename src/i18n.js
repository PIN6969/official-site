export function getStoredLang() {
  const saved = localStorage.getItem(STORAGE_KEY);
  if (saved && messages[saved]) return saved;
  return 'zh-TW';
}

export function setLang(lang) {
  if (!messages[lang]) return;
  localStorage.setItem(STORAGE_KEY, lang);
  applyTranslations(lang);
  document.documentElement.lang = HTML_LANG[lang] || 'zh-Hant';
  updateFlags(lang);
}

export function applyTranslations(lang) {
  const dict = messages[lang];
  if (!dict) return;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] != null) el.textContent = dict[key];
  });
}

export function updateFlags(lang) {
  const src = FLAG_SRC[lang] || FLAG_SRC['zh-TW'];
  document.querySelectorAll('[data-lang-flag]').forEach((img) => {
    img.setAttribute('src', src);
  });
}

export function initI18n() {
  const lang = getStoredLang();
  setLang(lang);
  document.querySelectorAll('[data-set-lang]').forEach((btn) => {
    btn.addEventListener('click', () => {
      const next = btn.getAttribute('data-set-lang');
      if (next) setLang(next);
    });
  });
}
